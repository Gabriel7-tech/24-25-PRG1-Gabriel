public class Prueba {
    public static void main(String[] args) {
        int dia = 6;
        switch (dia){
            case 1:
            System.out.println("Lunes");
            break;
            case 2:
            System.out.println("Martes");
            break;
            case 3:
            System.out.println("Miercoles");
            break;
            case 4:
            System.out.println("Jueves");
            break;
            case 5:
            System.out.println("Viernes");
            break;
            default:
            System.out.println("Fin de semana!");
        }
        }
    }